#[test]
fn codegen_toml_and_style_hook_are_isomorphic() {
    let toml = r#"
        profile = "api-client"

        [style]
        patch = false

        [types."Order"]
        patch = true
    "#;

    // The parsed config carries both settings.
    let config = StyleConfig::from_toml_str(toml, StyleConfig::api_client()).unwrap();
    assert!(!config.patch);
    assert_eq!(config.types["Order"].patch, Some(true));

    let dir = std::path::PathBuf::from(env!("CARGO_TARGET_TMPDIR")).join("patch_config");
    std::fs::create_dir_all(&dir).unwrap();
    let config_path = dir.join("iso.toml");
    std::fs::write(&config_path, toml).unwrap();

    let via_toml = generator_for("iso_toml", order_spec())
        .config_file(config_path)
        .generate_to_string()
        .unwrap();
    let via_hook = generator_for("iso_hook", order_spec())
        .style(|style| {
            style.patch = false;
            style.types.entry("Order".to_string()).or_default().patch = Some(true);
        })
        .generate_to_string()
        .unwrap();
    // Bodies must match byte-for-byte (headers name different temp spec
    // paths).
    assert_eq!(
        body(&via_toml),
        body(&via_hook),
        "config file and style() hook diverge",
    );
}
